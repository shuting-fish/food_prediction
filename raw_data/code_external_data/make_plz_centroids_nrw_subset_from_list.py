def _detect_zip_column(df: pd.DataFrame) -> str:
    """Detect the zip/PLZ column name in the centroids file."""
    candidates = ["plz", "zip", "zipcode", "postal_code", "post_code"]
    lower_map = {c.lower(): c for c in df.columns}
    for cand in candidates:
        if cand in lower_map:
            return lower_map[cand]
    raise ValueError(f"Cannot detect PLZ column. Available columns: {df.columns.tolist()}")

def main() -> None:
    # Read the full centroids table as strings to preserve leading zeros.
    centroids = pd.read_csv(IN_CENTROIDS, dtype=str)

    zip_col = _detect_zip_column(centroids)
    centroids[zip_col] = centroids[zip_col].astype(str).str.strip().str.zfill(5)

    # Parse all 5-digit PLZ values from the raw text block.
    nrw_plz = sorted(set(re.findall(r"\b\d{5}\b", NRW_PLZ_RAW)))

    nrw_set = set(nrw_plz)
    subset = centroids[centroids[zip_col].isin(nrw_set)].copy()

    # Diagnostics: show missing PLZ (present in list but not in input centroids).
    found = set(subset[zip_col].unique())
    missing = sorted(nrw_set - found)

    # Write deterministic output (sorted by PLZ) for clean diffs in Git.
    subset = subset.sort_values(by=zip_col).reset_index(drop=True)
    OUT_CENTROIDS.parent.mkdir(parents=True, exist_ok=True)
    subset.to_csv(OUT_CENTROIDS, index=False)

    print(f"Wrote: {OUT_CENTROIDS}")
    print(f"- Input rows:   {len(centroids)}")
    print(f"- NRW list PLZ:  {len(nrw_plz)} (unique)")
    print(f"- Output rows:  {len(subset)}")
    if missing:
        # Limit console spam but keep it actionable.
        preview = ', '.join(missing[:50])
        more = f" (+{len(missing)-50} more)" if len(missing) > 50 else ""
        print(f"- Missing PLZ in centroids source: {len(missing)} -> {preview}{more}")
    else:
        print("- Missing PLZ in centroids source: 0")

if __name__ == "__main__":
    main()